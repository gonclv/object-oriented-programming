package ex4;

public class Endereco {
    private String logradouro;
    private int numero;
    private String bairro;
    private int cep;
    private String cidade;
    private String UF;
    
	public String getLogradouro() {
		return logradouro;
	}
	public int getNumero() {
		return numero;
	}
	public String getBairro() {
		return bairro;
	}
	public int getCep() {
		return cep;
	}
	public String getCidade() {
		return cidade;
	}
	public String getUF() {
		return UF;
	}
}